/***************************************************************************************
 *                                        DEPENDENCIES
 **************************************************************************************/
require('dotenv').config();
require('./config/database')
const express = require('express');

/***************************************************************************************
 *                                        APP CONFIG
 **************************************************************************************/
const { PORT } = process.env


const app = express()





/***************************************************************************************
 *                                        MIDDLEWARE
 **************************************************************************************/


/***************************************************************************************
 *                                        ROUTER MIDDELWARE
 **************************************************************************************/
app.get('/', (req, res)=> {
    res.send('hello world')
})


/***************************************************************************************
 *                                        SERVER
 **************************************************************************************/


app.listen(PORT, ()=> console.log(`Listening on port: ${PORT}`))